/* Function overload */

int Summa(int a, int b)
{
	return a + b;
}

double Summa(double a, double b)
{
	return a + b;
}

int Summa(int a, int b, int c)
{
	return a + b + c;
}

double Summa(int a, double b, int c)
{
	return a + b + c;
}

